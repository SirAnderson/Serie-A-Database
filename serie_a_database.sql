	WHERE id_squadra = NEW.id_squadra_casa;ELSE

	UPDATE squadra
	SET pareggi = pareggi + 1
	WHERE (id_squadra = NEW.id_squadra_casa);

	UPDATE squadra
	SET pareggi = pareggi + 1
	WHERE (id_squadra = NEW.id_squadra_trasferta);
	END IF;

	UPDATE squadra
